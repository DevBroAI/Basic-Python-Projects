# ---------------------------------------------------------------------------- #
#                  swapping value using temporary variable                     #
# ---------------------------------------------------------------------------- #
x = 8
y = 7
temp=x
x=y
y=temp
print(x)
print(y)

# ---------------------------------------------------------------------------- #
#              swapping values without using a temporary variable              #
# ---------------------------------------------------------------------------- #

a=10
b=11

a,b = b,a
print(a)
print(b)
